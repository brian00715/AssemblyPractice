DATA SEGMENT
	OUT1 DB 'ABCDE'
	COUNT = $ - OUT1
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA,ES:DATA
START:
	MOV AX,DATA
	MOV ES,AX
	MOV DS,AX

	MOV AX,0600H	;清屏
	MOV BH,07H
	MOV CX,0
	MOV DX,184FH
	INT 10H

	MOV AX,0200H	;移动光标
	MOV BH,0
	MOV DX,50
	INT 10H

	MOV AX,1300H	;显示字符串
	MOV BX,0002H
	MOV CH,0
	MOV CL,COUNT
	MOV DX,0
	LEA BP,OUT1
	INT 10H

	MOV AX,4C00H
	INT 21H
CODE ENDS
	END START